#################### Integer #################################
a = 123                  # Fixnum decimal
b = 1_234                # Fixnum decimal with underline
c = -500                 # Negative Fixnum
d = 0377                 # octal
e = 0xff                 # hexadecimal
f = 0b1011               # binary
g = ?a                   # character code for 'a'
h = ?\n                  # code for a newline (0x0a)
i = 12345678901234567890 # Bignum

puts a
puts b
puts c
puts d
puts e
puts f
puts g
puts h
puts i

################# floating numbers ##################
j = 123.4                # floating point value
k = 1.0e6                # scientific notation
l = 4E20                 # dot not required
m = 4e+20                # sign before exponential

puts j
puts k
puts l
puts m 
